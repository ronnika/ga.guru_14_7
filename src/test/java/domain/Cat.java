package domain;

public class Cat {
    private String name;
    private int age;
    private String owner;
    private String passportId;
    private String[] hobbies;


    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getOwner() {
        return owner;
    }

    public String getPassportId() {
        return passportId;
    }

    public String[] getHobbies() {
        return hobbies;
    }
}
